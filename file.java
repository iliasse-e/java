        // 32 bits : -2147483648 à 2147483647
        int c = 2222222;

        // 64 bits : -9223372036854775808 à 9223372036854775807
        long d = 33L;


        // Nombre à virgule

        // 32 bits : 1.401e-045 à 3.40282e+038
        float e = 1.3f;

        // 64 bits : 2.22507e-308 à 1.79769e+308
        double f = 2.3;


        // Caractères

        char g = 'g';


        // Chaines de caractères

        String h = g + "chaine";


        // Booléen

        boolean i = false;


